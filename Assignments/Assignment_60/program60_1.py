#########################################################################
# Problem Statement :
#       Write a python program to simulate a single Artificial Neuron
#       x1 = 2
#       x2 = 3
#       w1 = 0.4
#       w1 = 0.6
#       bias = 0.5
#########################################################################

import numpy as np
import math

#-------------------------------------------------------------------------
#   Step 1 : Define Input Features
#-------------------------------------------------------------------------

inputs = np.array([2,3])

#-------------------------------------------------------------------------
#   Step 2 : Define Weights
#-------------------------------------------------------------------------

weights = np.array([0.4, 0.6])

#-------------------------------------------------------------------------
#   Step 3 : Define Bias
#-------------------------------------------------------------------------

bias = 0.5

#-------------------------------------------------------------------------
#   Step 4 : Calculate Weighted Sum (Z)
#-------------------------------------------------------------------------

# Formula : Z = (x1*w1 + x2*w2) + bias

weighted_sum = np.dot(inputs, weights) + bias 

#-------------------------------------------------------------------------
#   Step 5 : Activattion Function (Sigmoid) -> Output range is (0 to 1)
#-------------------------------------------------------------------------

def sigmoid(z):
    ans = 1 / (1 + math.exp(-z))
    return ans

#-------------------------------------------------------------------------
#   Step 6 : Final Output 
#-------------------------------------------------------------------------

output = sigmoid(weighted_sum)

#-------------------------------------------------------------------------
#   Step 7 : Display Result
#-------------------------------------------------------------------------

print("Inputs           :", inputs)
print("weights          :", weights)
print("bias             :", bias)
print("weighted_sum (z) :", weighted_sum)
print("final output     :", output)
print("The output of this single neuron is greater than the threshold value so it becomes a  : 1")
